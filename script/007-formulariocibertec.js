{
    const bloque = document.querySelector('.a002-51-007');

    const letra = document.querySelector('.a002-51-55-007');

    const fueradelbloque = document.querySelector('form');

    const boton = document.querySelector('.a003-007');

    document.addEventListener('mouseup', function(e) {
        if ((!boton.contains(e.target))) {
            if((boton.value !==  "")){
                bloque.classList.add('a002-51-007none');
                letra.classList.add('a002-51-55-007none');
            }
            else if((boton.value ==  "")){
                bloque.classList.remove('a002-51-007none');
                letra.classList.remove('a002-51-55-007none');
            }
            else{
                alert('asu asu, que estara yendo mal?'); 
            }
        }
        else{
            bloque.classList.add('a002-51-007none');
            letra.classList.add('a002-51-55-007none');
        }
    });
}

